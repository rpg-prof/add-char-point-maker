export function getEnglishWeaponCode(name, usedCodes = new Set()) {
  let code = ENGLISH_CODE_BY_NAME[name];
  if (!code) {
    throw new Error(`Código em inglês não definido para: ${name}`);
  }
  if (!usedCodes.has(code)) {
    usedCodes.add(code);
    return code;
  }
  let suffix = 2;
  while (usedCodes.has(`${code}-${suffix}`)) suffix++;
  const unique = `${code}-${suffix}`;
  usedCodes.add(unique);
  return unique;
}
